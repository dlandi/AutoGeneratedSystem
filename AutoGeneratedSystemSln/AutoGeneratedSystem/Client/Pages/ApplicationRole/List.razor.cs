using AutoGeneratedSystem.ClientServices;
using AutoGeneratedSystem.Common;
using AutoGeneratedSystem.Models.ApplicationRole;
using Microsoft.AspNetCore.Components;
using Blazored.Toast.Services;
namespace AutoGeneratedSystem.Client.Pages.ApplicationRole
{
[Route(Constants.PageRoutes.ApplicationRoleRoutes.List)]
public partial class List
{
[Inject]
ApplicationRoleClientService ApplicationRoleClientService { get; set; }
[Inject]
private IToastService ToastService { get;set; }
private ApplicationRoleModel[] AllApplicationRole { get; set; }
private bool IsLoading {get; set;}
protected override async Task OnInitializedAsync()
{
try
{
this.IsLoading = true;
this.AllApplicationRole = await ApplicationRoleClientService.GetAllApplicationRoleAsync();
}
catch (Exception ex)
{
ToastService.ShowError(ex.Message);
}
finally
{
this.IsLoading=false;
}
}
}
}
